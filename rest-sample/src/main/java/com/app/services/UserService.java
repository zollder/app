package com.app.services;

import com.app.domain.User;

public interface UserService
{
	// --------------------------------------------------------------------------------------------------------------------------------
	/** Loads given user entity by primary key (id). Returns fetched contact. */
	// --------------------------------------------------------------------------------------------------------------------------------
	public User loadWithPrimaryKey(Long key);

	// --------------------------------------------------------------------------------------------------------------------------------
	/** Loads given user entity by userName. Returns fetched user. */
	// --------------------------------------------------------------------------------------------------------------------------------
	public User loadWithUserName(String userName);

	// --------------------------------------------------------------------------------------------------------------------------------
	/** Saves given User entity. Returns saved user. */
	// --------------------------------------------------------------------------------------------------------------------------------
	public User save(User user);

	// --------------------------------------------------------------------------------------------------------------------------------
	/** Updates given User entity. Returns updated user. */
	// --------------------------------------------------------------------------------------------------------------------------------
	public User update(User user);

	// --------------------------------------------------------------------------------------------------------------------------------
	/** Deletes given User entity. */
	// --------------------------------------------------------------------------------------------------------------------------------
	public void delete(Long key);

	// --------------------------------------------------------------------------------------------------------------------------------
	/** Refreshes saved User so ensure correct serialisation/marshalling (especially for @ManyToOne relations) */
	// --------------------------------------------------------------------------------------------------------------------------------
	public void refresh(User user);
}